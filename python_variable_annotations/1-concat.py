#!/usr/bin/env python3
"""concat"""


def concat(str1: str, str2: str) -> str:
    """Concatenates two strings

    Args:
        str1 (str): First string to concatenate
        str2 (str): Second string to concatenate

    Returns:
        str: Resulting string from the concatenation of str1 and str2
    """
    return str1 + str2
